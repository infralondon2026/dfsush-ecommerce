export const dictionaries: Record<Lang, Dict> = { es, pt, en };

export const LANGS: Lang[] = ['es', 'pt', 'en'];

const LANG_STORAGE_KEY = 'dfsas_lang';

export function getInitialLang(): Lang {
  try {
    const saved = localStorage.getItem(LANG_STORAGE_KEY);
    if (saved === 'es' || saved === 'pt' || saved === 'en') return saved;
  } catch {
    /* localStorage puede no estar disponible */
  }
  const nav = typeof navigator !== 'undefined' ? navigator.language.toLowerCase() : 'es';
  if (nav.startsWith('pt')) return 'pt';
  if (nav.startsWith('en')) return 'en';
  return 'es';
}

export function persistLang(lang: Lang): void {
  try {
    localStorage.setItem(LANG_STORAGE_KEY, lang);
  } catch {
    /* noop */
  }
}

const LOCALE_BY_LANG: Record<Lang, string> = { es: 'es-AR', pt: 'pt-BR', en: 'en-US' };

export function formatUsd(amount: number, lang: Lang): string {
  return new Intl.NumberFormat(LOCALE_BY_LANG[lang], {
    style: 'currency',
    currency: 'USD',
    currencyDisplay: 'narrowSymbol',
  }).format(amount);
}

/** Formatea una fecha ISO/`YYYY-MM-DD` en la zona horaria de Ushuaia. */
export function formatDate(iso: string, lang: Lang, withTime = false): string {
  const date = iso.length === 10 ? new Date(`${iso}T12:00:00-03:00`) : new Date(iso);
  return new Intl.DateTimeFormat(LOCALE_BY_LANG[lang], {
    timeZone: 'America/Argentina/Ushuaia',
    day: '2-digit',
    month: '2-digit',
    year: 'numeric',
    ...(withTime ? { hour: '2-digit', minute: '2-digit' } : {}),
  }).format(date);
}

/** Fecha de hoy (YYYY-MM-DD) en la zona America/Argentina/Ushuaia, no UTC. */
export function todayInUshuaia(): string {
  return new Intl.DateTimeFormat('en-CA', {
    timeZone: 'America/Argentina/Ushuaia',
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
  }).format(new Date());
}

/** Suma días a una fecha YYYY-MM-DD (aritmética de calendario, sin TZ). */
export function addDays(ymd: string, days: number): string {
  const d = new Date(`${ymd}T12:00:00Z`);
  d.setUTCDate(d.getUTCDate() + days);
  return d.toISOString().slice(0, 10);
}
